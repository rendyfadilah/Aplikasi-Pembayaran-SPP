                                               <p><p>1)&nbsp;&nbsp; Melaksanakan penyelarasan kurikulum dengan IDUKA</p>
<p>2)&nbsp;&nbsp; Melaksanakan pengembangan model pembelajaran</p>
<p>3)&nbsp;&nbsp; Mengimplementasikan nilai-nilai luhur budaya dan agama sebagai karakter bangsa</p>
<p>4)&nbsp;&nbsp; Membina dan mengembangkan bakat dan potensi peserta didik dalam mengembangkan kecakapan hidup</p>
<p>5)&nbsp;&nbsp; Melaksanakan pembinaan prestasi akademik dan non akademik peserta didik sesuai minat dan kompetensi</p>
<p>6)&nbsp;&nbsp; Mengembangkan sikap kemandirian dan interprenership peserta didik</p>
<p>7)&nbsp;&nbsp; Melaksanakan uji sertifikasi kompetensi peserta didik oleh lembaga yang kompeten</p>
<p>8)&nbsp;&nbsp; Meningkatkan ketersediaan fasilitas dan sarana prasarana yang mendukung proses pembelajaran</p>
<p><em>9)&nbsp;&nbsp; </em>Meningkatkan kompetensi tenaga pendidik dan kependidikan <em>reskilling</em> dan <em>upskilling</em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p>10) Meningkatkan kebekerjaan lulusan melalui kerjasama dengan IDUKA dan Bursa Kerja Khusus ( BKK )</p>
<p>11) Mengelola sumber dana sekolah sesuai peruntukan secara akuntabel dan transparan</p>
<p>12) Menerapkan sistem pengelolaan lembaga sesuai standar Sistem Manajemen Mutu ISO 9001</p> </p>                                          

																		
						
						
